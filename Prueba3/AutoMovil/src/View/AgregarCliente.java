    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        ClienteControlador cl = new ClienteControlador();
        
        //Definir los PArametros que se agregaran
        int rut = Integer.parseInt(txtRut.getText());
        String nombre = txtNombre.getText();
        int numeroContacto = Integer.parseInt(txtNumeroContacto.getText());
        String direccion = txtDireccion.getText();
        String tipoCliente =txtTipoCliente.getText();
        
        cl.agregarCliente(rut, nombre, numeroContacto, direccion, tipoCliente);
        helper.showInformation("Cliente agregado al SISTEMA");
        limpiarControles();
    }//GEN-LAST:event_btnAgregarActionPerformed

