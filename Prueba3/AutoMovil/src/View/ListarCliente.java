    public void cargarTabla(){
        ClienteControlador cc = new ClienteControlador();
        DefaultTableModel modelo = (DefaultTableModel)tblListadoClientes.getModel();
        modelo.setRowCount(0);//resetea las filas existentes en la tabla
        
        for(Cliente i: cc.obtenerCliente()){
            //a√±ade una fila a la tabla con los datos de Cliente
            modelo.addRow(new Object[]{
                i.getRut(),
                i.getNombre(),
                i.getNumero_contacto(),
                i.getDireccion(),
                i.getTipo_cliente()
            });
        }
    }
    
    
    
